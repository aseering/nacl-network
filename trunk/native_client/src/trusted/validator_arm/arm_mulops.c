void BuildArmMulopInstructions() {
  int i;
  for (i = 0; ; ++i) {
    const ModeledOpInfo* op = &(kMulOps[i]);
    if (op->inst_type == ARM_INST_TYPE_SIZE) break;
    AddInstruction(op);
  }
}
